CREATE DATABASE proofix;
USE proofix;

CREATE TABLE couriers (
	courier_id BIGINT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
	courier_fio VARCHAR(50)
);

CREATE TABLE regions (
	region_id BIGINT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
	region_name VARCHAR(50),
	time_to INT(50),
	time_from INT(50)
);

CREATE TABLE timetables (
	id BIGINT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
	region VARCHAR(50),
	courier VARCHAR(50),
	departure TIMESTAMP,
	arrival TIMESTAMP
);

